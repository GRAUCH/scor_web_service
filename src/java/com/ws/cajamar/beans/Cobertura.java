package com.ws.cajamar.beans;

public class Cobertura {
	
	String cobern;
	String vacobe;
	String sobpri;
	String sobmor;
	
	public String getCobern() {
		return cobern;
	}
	public void setCobern(String cobern) {
		this.cobern = cobern;
	}
	public String getVacobe() {
		return vacobe;
	}
	public void setVacobe(String vacobe) {
		this.vacobe = vacobe;
	}
	public String getSobpri() {
		return sobpri;
	}
	public void setSobpri(String sobpri) {
		this.sobpri = sobpri;
	}
	public String getSobmor() {
		return sobmor;
	}
	public void setSobmor(String sobmor) {
		this.sobmor = sobmor;
	}
	
}
